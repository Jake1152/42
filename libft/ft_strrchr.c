#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	idx;

	idx = ft_strlen(s);
	while (&s[idx] >= s)
	{
		if (s[idx] == (char)c)
			return ((char *)&s[idx]);
		idx--;
	}
	return (0);
}
